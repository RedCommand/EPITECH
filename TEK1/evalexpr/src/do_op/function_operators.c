int op_sum(int a, int b)
{
    return (a + b);
}

int op_diff(int a, int b)
{
    return (a - b);
}

int op_prod(int a, int b)
{
    return (a * b);
}

int op_div(int a, int b)
{
    return (a / b);
}

int op_mod(int a, int b)
{
    return (a % b);
}
